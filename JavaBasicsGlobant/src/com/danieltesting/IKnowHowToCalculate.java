package com.danieltesting;

public class IKnowHowToCalculate {

    public static double diameterCircle(double r){

        double diameter = 2.0*r;
        return diameter;
    }
    public static double areaCircle(double r){

        double area = Math.PI*Math.pow(r,2);
        return area;
    }
    public static double circunferenceCircle(double r){

        double circunference=2.0*Math.PI*r;
        return circunference;
    }


}
